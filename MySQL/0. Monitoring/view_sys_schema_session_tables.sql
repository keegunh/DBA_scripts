-- 각 클라이언트 연결에 대해 SSL 버전 및 암호화 방식(Cipher), SSL 세션 재사용 횟수 정보를 확인
SELECT thread_id
     , ssl_version
     , ssl_cipher
     , ssl_sessions_reused
  FROM sys.session_ssl_status;