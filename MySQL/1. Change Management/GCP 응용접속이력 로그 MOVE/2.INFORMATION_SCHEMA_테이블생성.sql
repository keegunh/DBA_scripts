  , EXTRA	varchar(256)
  , PRIVILEGES	varchar(154)
  , COLUMN_COMMENT	text NOT NULL
  , GENERATION_EXPRESSION	longtext NOT NULL
  , SRS_ID	int(10) unsigned
  , PRIMARY KEY (TABLE_CATALOG, TABLE_SCHEMA, TABLE_NAME, ORDINAL_POSITION)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_bin COMMENT='INFORMATION_SCHEMA.COLUMNS';

