USE STG_PORTAL;
CREATE TABLE `TABLES` (
    TABLE_CATALOG	varchar(64) NOT NULL
  , TABLE_SCHEMA	varchar(64) NOT NULL
  , TABLE_NAME	varchar(64) NOT NULL
  , TABLE_TYPE	enum('BASE TABLE','VIEW','SYSTEM VIEW') NOT NULL
  , ENGINE	varchar(64)
  , VERSION	int(2)
  , ROW_FORMAT	enum('Fixed','Dynamic','Compressed','Redundant','Compact','Paged')
  , TABLE_ROWS	bigint(21) unsigned
  , AVG_ROW_LENGTH	bigint(21) unsigned
  , DATA_LENGTH	bigint(21) unsigned
  , MAX_DATA_LENGTH	bigint(21) unsigned
  , INDEX_LENGTH	bigint(21) unsigned
  , DATA_FREE	bigint(21) unsigned
  , AUTO_INCREMENT	bigint(21) unsigned
  , CREATE_TIME	timestamp NOT NULL
  , UPDATE_TIME	datetime
  , CHECK_TIME	datetime
  , TABLE_COLLATION	varchar(64)
  , CHECKSUM	bigint(21)
  , CREATE_OPTIONS	varchar(256)
  , TABLE_COMMENT	text
  , PRIMARY KEY (TABLE_CATALOG, TABLE_SCHEMA, TABLE_NAME, TABLE_TYPE)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_bin COMMENT='INFORMATION_SCHEMA.TABLES';

CREATE TABLE `COLUMNS` (
    TABLE_CATALOG	varchar(64) NOT NULL
  , TABLE_SCHEMA	varchar(64) NOT NULL
  , TABLE_NAME	varchar(64) NOT NULL
