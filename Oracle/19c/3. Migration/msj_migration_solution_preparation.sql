					    AND B.INDEX_NAME = C.INDEX_NAME
					    AND B.OWNER = C.OWNER
					    AND C.CONSTRAINT_TYPE='P'
					 )
 ORDER BY TABLE_NAME,INDEX_NAME,COLUMN_POSITION;

