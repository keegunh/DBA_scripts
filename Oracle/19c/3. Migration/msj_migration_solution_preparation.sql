						    DISTINCT B.INDEX_NAME
					   FROM PMPBMIG.MSJ_MIG_TABLE A
						  , DBA_INDEXES           B
						  , DBA_CONSTRAINTS       C
					  WHERE A.TOBE_TABLE_NAME = B.TABLE_NAME
					    AND (A.MIG_TYPE IN ('A','B','C') OR A.MIG_TYPE IS NULL)
					    AND B.OWNER ='PMPBADM'
