-- 30MB TEST TABLE
CREATE TABLE CUST
AS
SELECT ROWNUM AS CUSTNO
     , '2024' || LPAD(CEIL(ROWNUM/100000), 2, '0') AS SALES_MONTH
	 , DECODE(MOD(ROWNUM, 12), 1, 'A', 'B') AS SALES_CD
	 , ROUND(DBMS_RANDOM.VALUE(1000, 100000), -2) AS SALES_REVENUE
	 , DECODE(SIGN_ROWNUM-100000), 1, 'M', 'F') AS GENDER
  FROM DUAL
CONNECT BY LEVEL <= 1200000;